"""
str - string
"""

print('Alguma coisa')  # Pode ser usado aspas simples ou duplas
print("Aspas duplas")

print("Essa é uma 'string' (str)")  # Se eu quiser aspas dentro da string vovê pode combinar as aspas

print("Esse é o meu \"texto\" (str)")  # Utilizando caracter de escape, problema que o \ usa algumas funções como o \n

print(r'Esse é o meu \n texto (str)')  # O r usa o raw e ignora as funções



