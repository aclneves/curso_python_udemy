"""
For in em Python
Iterando strings com for
Função range (start=0, stop, step=1)
"""

texto = 'I love Python'

for letra in texto:
    print(letra)

print()

for numero in range(10):
    print(numero)

print()

for numero in range(0, 50, 5):
    print(numero)

print()

for numero in range(10, 0, -1):
    print(numero)
